def main():
    num1 = input("Enter the first number: ")
    num2 = input("Enter the second number: ")
    outputmul = float(num1) * float(num2)
    outputdiv = float(num1) / float(num2)
    print("The product of the two numbers is:", outputmul)
    print("The quotient of the two numbers is:", outputdiv)

if __name__ == "__main__":
    main()