def main():
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    outputadd = num1 + num2
    outputsub = num1 - num2
    print("The sum of the two numbers is:", outputadd)
    print("The difference of the two numbers is:", outputsub)

if __name__ == "__main__":
    main()